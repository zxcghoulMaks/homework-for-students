/*
    Перевірка числа (if/else):
    Дано число. Якщо воно більше 0 — вивести "Додатнє".
    Якщо менше 0 — "Від’ємне".
    Якщо дорівнює 0 — "Нуль".
*/

const num = 5;

if (num > 0) {
    console.log("Додатнє");
} else if (num < 0) {
    console.log("Від’ємне");
} else {
    console.log("Нуль");
}